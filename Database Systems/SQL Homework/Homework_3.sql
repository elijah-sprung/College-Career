SELECT 
    SUM(NUM_INVOICES) AS 'Total Invoices',
    SUM(TOTAL_PURCHASES) AS 'Total Sales',
    MIN(TOTAL_PURCHASES) AS 'Minimum Customer Purchases',
    MAX(TOTAL_PURCHASES) AS 'Largest Customer Purchases',
    AVG(TOTAL_PURCHASES) AS 'Average Customer Purchases'
FROM
    (SELECT 
        CUS_CODE,
            COUNT(DISTINCT INVOICE.INV_NUMBER) AS NUM_INVOICES,
            SUM(LINE_UNITS * LINE_PRICE) AS TOTAL_PURCHASES
    FROM
        INVOICE
    JOIN LINE ON INVOICE.INV_NUMBER = LINE.INV_NUMBER
    GROUP BY CUS_CODE) AS DETAILS;

