    MIN(CUSTOMER.CUS_BALANCE) AS 'Minimum Balance',
    MAX(CUSTOMER.CUS_BALANCE) AS 'Maximum Balance',
    AVG(CUSTOMER.CUS_BALANCE) AS 'Average Balance'
FROM
    CUSTOMER
WHERE
    CUSTOMER.CUS_CODE IN (SELECT 
            CUS_CODE
        FROM
            INVOICE);
    
