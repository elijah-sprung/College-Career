    PRODUCT.P_DESCRIPT,
    PRODUCT.P_QOH,
    PRODUCT.P_PRICE,
    PRODUCT.P_QOH * PRODUCT.P_PRICE AS SUBTOTAL
FROM
    PRODUCT
ORDER BY SUBTOTAL DESC;
    
