SELECT DISTINCT
    CUSTOMER.CUS_CODE, CUSTOMER.CUS_BALANCE
FROM
    CUSTOMER
        JOIN
    INVOICE USING (CUS_CODE)
ORDER BY CUSTOMER.CUS_CODE;

